package home1;
//5+5、5+'5'、5+"5"
public class MyHomework6 {

	public static void main(String [] args) {
		System.out.println(5+5);    //代表為兩個整數的和，所以結果為整數10
		System.out.println(5+'5');  //為整數與字元相加。''代表char字元，'5'對應unicode的數字為53。所以結果為5+53=58。
		System.out.println(5+"5");  //為整數與字串相加。""代表String字串，表示整數5串接字串5。所以結果為55。
		
	}
}
